//Задача 54: Задайте двумерный массив. Напишите программу, которая упорядочит по убыванию элементы каждой строки двумерного массива.
//Например, задан массив:
//1 4 7 2
//5 9 2 3
//8 4 2 4
//В итоге получается вот такой массив:
//7 4 2 1
//9 5 3 2
//8 4 4 2

using System;

class Program
{
    static void Main()
    {
        // Задаем двумерный массив
        int[,] массив = {
            {1, 4, 7, 2},
            {5, 9, 2, 3},
            {8, 4, 2, 4}
        };

        // Проходим по каждой строке массива
        for (int i = 0; i < массив.GetLength(0); i++)
        {
            // Создаем временный массив для сортировки
            int[] временныйМассив = new int[массив.GetLength(1)];

            // Копируем элементы строки во временный массив
            for (int j = 0; j < массив.GetLength(1); j++)
            {
                временныйМассив[j] = массив[i, j];
            }

            // Сортируем элементы временного массива в порядке убывания
            Array.Sort(временныйМассив);
            Array.Reverse(временныйМассив);

            // Копируем отсортированные элементы обратно в исходную строку
            for (int j = 0; j < массив.GetLength(1); j++)
            {
                массив[i, j] = временныйМассив[j];
            }
        }

        // Выводим упорядоченный массив
        for (int i = 0; i < массив.GetLength(0); i++)
        {
            for (int j = 0; j < массив.GetLength(1); j++)
            {
                Console.Write(массив[i, j] + " ");
            }
            Console.WriteLine();
        }
    }
}





//Задача 56: Задайте прямоугольный двумерный массив. Напишите программу, которая будет находить строку с наименьшей суммой элементов.

//Например, задан массив:
//1 4 7 2
//5 9 2 3
//8 4 2 4
//5 2 6 7
//Программа считает сумму элементов в каждой строке и выдаёт номер строки с наименьшей суммой элементов: 1 строка


using System;

class Program
{
    static void Main()
    {
        // Задаем прямоугольный двумерный массив
        int[,] массив = {
            {1, 4, 7, 2},
            {5, 9, 2, 3},
            {8, 4, 2, 4},
            {5, 2, 6, 7}
        };

        // Инициализируем переменные для хранения индекса строки с наименьшей суммой и самой минимальной суммы
        int индексСтрокиСМинСуммой = 0;
        int минимальнаяСумма = int.MaxValue;

        // Проходим по каждой строке массива и считаем сумму элементов
        for (int i = 0; i < массив.GetLength(0); i++)
        {
            int суммаСтроки = 0;
            for (int j = 0; j < массив.GetLength(1); j++)
            {
                суммаСтроки += массив[i, j];
            }

            // Если текущая сумма меньше минимальной, обновляем минимальную сумму и индекс строки
            if (суммаСтроки < минимальнаяСумма)
            {
                минимальнаяСумма = суммаСтроки;
                индексСтрокиСМинСуммой = i;
            }
        }

        // Выводим номер строки с наименьшей суммой элементов
        Console.WriteLine("Строка с наименьшей суммой элементов: " + индексСтрокиСМинСуммой);
    }
}




//Задача 58: Задайте две матрицы. Напишите программу, которая будет находить произведение двух матриц.
//Например, даны 2 матрицы:
//2 4 | 3 4
//3 2 | 3 3
//Результирующая матрица будет:
//18 20
//15 18

using System;

class Program
{
    static void Main()
    {
        // Задаем матрицу A
        int[,] матрицаA = {
            {2, 4},
            {3, 2}
        };

        // Задаем матрицу B
        int[,] матрицаB = {
            {3, 4},
            {3, 3}
        };

        // Проверяем, можно ли умножить матрицы
        if (матрицаA.GetLength(1) != матрицаB.GetLength(0))
        {
            Console.WriteLine("Умножение невозможно. Количество столбцов матрицы A не совпадает с количеством строк матрицы B.");
            return;
        }

        // Создаем результирующую матрицу
        int[,] результирующаяМатрица = new int[матрицаA.GetLength(0), матрицаB.GetLength(1)];

        // Вычисляем произведение матриц
        for (int i = 0; i < матрицаA.GetLength(0); i++)
        {
            for (int j = 0; j < матрицаB.GetLength(1); j++)
            {
                int сумма = 0;
                for (int k = 0; k < матрицаA.GetLength(1); k++)
                {
                    сумма += матрицаA[i, k] * матрицаB[k, j];
                }
                результирующаяМатрица[i, j] = сумма;
            }
        }

        // Выводим результирующую матрицу
        Console.WriteLine("Результирующая матрица:");
        for (int i = 0; i < результирующаяМатрица.GetLength(0); i++)
        {
            for (int j = 0; j < результирующаяМатрица.GetLength(1); j++)
            {
                Console.Write(результирующаяМатрица[i, j] + " ");
            }
            Console.WriteLine();
        }
    }
}


